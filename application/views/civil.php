                        <h4 class="text-center upper">About Civil Engineering</h4>
                            <p>The Department of Civil Engineering at Vaageswari College of Engineering is producing high quality technical manpower needed to industry, R&amp;D organizations, and academic institutions since 2013 with an Intake of 60. The intake has been increased to 120 in the year 2014.</p>
                            <p>Under academic activity, more emphasis is bestowed on deep comprehension and understanding of fundamental principles of each subject in all cognitive teaching learning methodologies. The department has full-fledged Surveying laboratory, AutoCAD, GIS laboratory, Engineering Geology laboratory, Strength of materials laboratory, Hydraulic and Hydraulics machinery laboratory, Soil Mechanics laboratory, Concrete Technology laboratory, Environmental Engineering laboratory, Transportation Engineering laboratory.</p>
                            <p>Many of our students are placed in reputed companies like GMR, L&amp;T, Aparna infrastructures, NCC, Navayuga, GVK, Lanco Infratech limited, IVRCL infrastructures &amp; projects ltd, Indian army, Indian navy and many more. The academic activities of the department emphasize deep understanding of fundamental principles, development of creative ability to handle the challenge of civil engineering, and the analytical ability to solve problems which are interdisciplinary in nature. The department is actively involved in basic and applied research and consultancy.</p>
                            <p>Department also provides technical guidelines for the benefits of students and entrepreneurs by organizing short-term courses, certificate oriented training programs like Auto-cad, Stadd.pro, and Primavera,Revit,MEP etc., under finishing school concept, seminars, academic/industrial guest lectures, national level workshops, national and international conferences.</p>
                        <h3>Vision</h3>
                            <p align="justify">Vision is to become school of excellence that brings out civil engineers with technical competencies and promotes high-end research to meet the current and future challenges. To establish an outstanding centre of regional for providing a quality engineering education to the students from the rural area, excellent research and services to the professional and the community, to produce quality, employee principles of continual quality improvement to enhance its programme and faculty</p>
                        <h3>Mission</h3>
                            <p>Striving to be centre of excellence in civil engineering education to provide students the latest learning techniques and a complete knowledgebase for sustainable development of society. To be an outstanding devoted to provide high end research and technical education which will produce socially aware professionals to provide solutions to global community.</p>
                            <p>Provide value based technical education and empower the students to become competent professionals, preparing them for entry-level positions in department ,life-long learning,and societal leadership. Allied with both the school of engineering and the collegeâ€™s mission. The department is dedicated ti providing a dynamic learning environment that emphasizes open ended design,problem-solving skills,team work,communication, and leadership skills.</p>
                            To Accomplish the Mission:
                            <ol>
                                <li>An exceptional curriculum including in depth coverage in technical sub-disciplines of the department.</li>
                                <li>Engages the students in creating innovative design solutions that include realistic constraints such as economic, environmental,social,health &amp; safety.</li>
                                <li>To train the students in a manner that they can pursue higher studies so that they can contribute to the teaching profession and development of civil engineering and other allied fields.</li>
                                <li>The multicultural and multi-disciplinary groups for sustainable development and growth.</li>
                            </ol>
                        <h3>Objectives:</h3>
                            <ol>
                                <li>To make the students useful for Building Industry directly after completing Civil Engineering course.</li>
